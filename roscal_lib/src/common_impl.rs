use crate::{assessment::*, control::*, implementation::*};

macro_rules! impl_conversion {
    ( $( $t:ty ),* ) => {
        $(
            impl AsRef<$t> for $t {
                fn as_ref(&self) -> &Self {
                    self
                }
            }

            impl AsMut<$t> for $t {
                fn as_mut(&mut self) -> &mut Self {
                    self
                }
            }

            impl From<&$t> for $t {
                fn from(value: &$t) -> Self {
                    value.clone()
                }
            }

            impl std::fmt::Display for $t {
                fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
                    write!(f, "{self:?}")
                }
            }
        )*
    };
}

